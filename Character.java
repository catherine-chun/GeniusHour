
public class Character {
	private String name;
	private int age;
	
	public Character(String name1, int age1)
	{
		name=name1;
		age=age1;
	}
	
	public void setName(String name1)
	{
		name=name1;
		
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setAge(int age1)
	{
		age=age1;
	}
	
	public int getAge()
	{
		return age;
	}
	
	

}
